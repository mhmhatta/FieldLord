        if(question == JOptionPane.OK_OPTION){
            try{
                PreparedStatement ps;
                String sql = "DELETE FROM penyewaan WHERE id = ?";
                ps = MyConnection.getConnection().prepareStatement(sql);
                ps.setString(1, id);
                ps.executeUpdate();
                ps.close();
                JOptionPane.showMessageDialog(null, "Data has Deleted");
            }catch(SQLException e){
                System.out.println("Failed Occuredf");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(CRUD_rental.class.getName()).log(Level.SEVERE, null, ex);
            }finally{
                loadData();
            }
        }
        if(question == JOptionPane.CANCEL_OPTION){
        }
