    public void loadData(){
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try{
            PreparedStatement ps;
            ResultSet rs;
            String sql = "SELECT penyewaan.id, user.nama, lapangan.nama_lapangan, jadwal.jadwal_booking, jadwal.jam, kode_booking FROM penyewaan, lapangan, jadwal, user WHERE user.id=penyewaan.id_penyewa AND lapangan.id=penyewaan.id_lapangan AND jadwal.id=penyewaan.id_jadwal";
            ps = MyConnection.getConnection().prepareStatement(sql);
            rs = ps.executeQuery();
            while(rs.next()){
                Object[] o = new Object[6];
                o [0] = rs.getString("penyewaan.id");
                o [1] = rs.getString("user.nama");
                o [2] = rs.getString("lapangan.nama_lapangan");
                o [3] = rs.getString("jadwal.jadwal_booking");
                o [4] = rs.getString("jadwal.jam");
                o [5] = rs.getString("kode_booking");
                model.addRow(o);
            }
            rs.close();
            ps.close();
        }catch(SQLException e){
            System.out.println("Failed Occured");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CRUD_rental.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
