    private void autonumber(){
        try{
            PreparedStatement ps;
            ResultSet rs;
            
            String sql = "SELECT * FROM penyewaan ORDER BY kode_booking DESC";
            
            ps = MyConnection.getConnection().prepareStatement(sql);
            rs = ps.executeQuery();

            if (rs.next()){
                String Kode = rs.getString("kode_booking").substring(2);
                String PY = "" + (Integer.parseInt(Kode) + 1);
                String Nol = "";
                
                if(PY.length()==1){
                    Nol = "00";
                }else if(PY.length()==2){
                    Nol = "0";
                }else if(PY.length()==3){
                    Nol = "";
                }
                
                jKode.setText("PY" + Nol + PY);
                
            }else{
                jKode.setText("PY001");
            }
            rs.close();
            ps.close();
        }catch(Exception e){
            System.out.println("Autonumber Error");
        }
    }
    
    public void loadData(){
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try{
            PreparedStatement ps;
            ResultSet rs;
            String sql = "SELECT lapangan.nama_lapangan, jadwal_booking, jam FROM jadwal, lapangan WHERE lapangan.id = jadwal.id_lapangan AND status='Available'";
            ps = MyConnection.getConnection().prepareStatement(sql);
            rs = ps.executeQuery();
            while(rs.next()){
                Object[] o = new Object[5];
                o [0] = rs.getString("lapangan.nama_lapangan");
                o [1] = rs.getString("jadwal_booking");
                o [2] = rs.getString("jam");
                model.addRow(o);
            }
            rs.close();
            ps.close();
        }catch(SQLException e){
            System.out.println("Error Occured!");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(create_rent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
