    private void UpdateStatus() {
        try{
            PreparedStatement ps;
            String jam = jJam.getText();
            String hari = jHari.getText();
            String lapangan = jLapangan.getText();
            String status = "Tidak Tersedia";
            String sql = "UPDATE jadwal SET status='"+status+"' WHERE jam='"+jam+"' AND id_lapangan=(SELECT id FROM lapangan WHERE nama_lapangan='"+lapangan+"') AND jadwal_booking='"+hari+"'";
            ps = MyConnection.getConnection().prepareStatement(sql);
            ps.executeUpdate();
            ps.close();
            JOptionPane.showMessageDialog(null, "Status Has Beed Changed");
        }catch(SQLException e){
            System.out.println("Error Occured");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(create_rent.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            loadData();
            autonumber();
        }
    }
    
    private void jSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSaveActionPerformed
        String user = jUser.getText();
        String kode = jKode.getText();
        String lapangan = jLapangan.getText();
        String hari = jHari.getText();
        String jam = jJam.getText();

        try{
            PreparedStatement ps;
            String sql = "INSERT INTO `penyewaan` (`id_penyewa`, `id_lapangan`, `id_jadwal`, `kode_booking`) VALUES ((SELECT id FROM user WHERE username = ?), (SELECT id FROM lapangan WHERE nama_lapangan = ?), (SELECT id FROM jadwal WHERE jadwal_booking = ? AND jam = ?), ?)";
            ps = MyConnection.getConnection().prepareStatement(sql);
            ps.setString(1, user);
            ps.setString(2, lapangan);
            ps.setString(3, hari);
            ps.setString(4, jam);
            ps.setString(5, kode);
            ps.executeUpdate();
            ps.close();
            JOptionPane.showMessageDialog(null, "Rent Successfully");
        }catch(SQLException e){
            System.out.println("Error Occured");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(create_rent.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            loadData();
            autonumber();
            UpdateStatus();
        }
    }//GEN-LAST:event_jSaveActionPerformed

